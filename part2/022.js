console.log(5 == 5);
console.log("5" == 5);  // 형 변환하여 비교
console.log(5 == '5');  // 형 변환하여 비교
console.log('------');
console.log(5 != 1);
console.log(5 != "1");  // 형 변환하여 비교
console.log(5 != "5");  // 형 변환하여 비교
console.log('------');
console.log(5 === 5);   // === 은 자료형 일치 여부까지 비교
console.log(5 === "5");
console.log('------');
console.log(5 !== 10);  // !== 엄격한 기준, 값과 자료형 비교
console.log(5 !== "5");
